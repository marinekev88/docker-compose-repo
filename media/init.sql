GRANT ALL PRIVILEGES ON 'Ombi_Settings'.* TO 'ombi'@'%' WITH GRANT OPTION;
GRANT ALL PRIVILEGES ON 'Ombi_External'.* TO 'ombi'@'%' WITH GRANT OPTION;